_DF = None
_NDOCS = 0


def _tokenize(s):
    try:
        return [t for t in re.findall(r"[가-힣A-Za-z0-9]+", str(s)) if len(t) >= 2]
    except Exception:
        return []


def _ngrams(word, n=2):
    try:
        w = str(word)
        if len(w) < n:
            return [w] if w else []
        return [w[i:i + n] for i in range(len(w) - n + 1)]
    except Exception:
        return []


def _build_df():
    """글자 2-gram의 문서빈도(DF)를 1회 계산. 실패해도 안전."""
    global _DF, _NDOCS
    if _DF is not None:
        return
    df = {}
    try:
        _NDOCS = len(GUIDELINE_INDEX)
        for item in GUIDELINE_INDEX:
            txt = item.get("text", "")
            seen = set()
            for i in range(len(txt) - 1):
                seen.add(txt[i:i + 2])
            for g in seen:
                df[g] = df.get(g, 0) + 1
    except Exception:
        df = {}
    _DF = df


def _idf(g):
    try:
        if not _DF or _NDOCS <= 0:
            return 1.0
        return math.log((_NDOCS + 1) / (_DF.get(g, 0) + 1)) + 1.0
    except Exception:
        return 1.0


def _query_concepts(query):
    """질의 → [(핵심단어, 동의어 포함 2-gram 집합), ...]."""
    words = [w for w in _tokenize(query) if w not in _STOP]
    concepts = []
    for w in words:
        terms = [w] + _SYNONYMS.get(w, [])
        grams = set()
        for t in terms:
            for tok in _tokenize(t):
                for g in _ngrams(tok, 2):
                    grams.add(g)
        concepts.append((w, grams))
    return concepts


def _search_guidelines(query, topk=5, source_keyword=""):
    """개념 커버리지 우선 + IDF 가중 검색.

    질의가 담은 여러 개념을, 각 개념의 '가장 특징적인(희귀한) 글자'로 정확히 맞힌
    문단을 우선한다. 흔한 글자는 약하게, 희귀한 글자(2분의 등)는 강하게 반영.
    단어가 달라도(부모위원 ↔ 학부모 대표) 글자로 찾아낸다. 절대 예외 없음.
    """
    try:
        _build_df()
        concepts = _query_concepts(query)
        if not concepts or not GUIDELINE_INDEX:
            return []
        scored = []
        for item in GUIDELINE_INDEX:
            try:
                txt = item.get("text", "")
                src = item.get("source", "")
                if source_keyword and source_keyword not in src:
                    continue
                score = 0.0
                any_match = False
                for w, grams in concepts:
                    best = 0.0
                    for g in grams:
                        if g in txt:
                            idf = _idf(g)
                            if idf > best:
                                best = idf
                    if best > 0:
                        any_match = True
                        score += best
                    if len(w) >= 2 and w in txt:
                        score += 2.0
                if not any_match:
                    continue
                scored.append((score, item))
            except Exception:
                continue
        scored.sort(key=lambda x: -x[0])
        out = []
        for _score, item in scored[:topk]:
            snippet = re.sub(r"\s+", " ", item.get("text", "")).strip()
            out.append({"source": item.get("source", "지침서"), "text": snippet[:500]})
        return out
    except Exception:
        return []


