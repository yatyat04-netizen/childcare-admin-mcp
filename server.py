LAW_OC = os.environ.get("LAW_GO_KR_OC", "yatyat4542")


def _fetch_law_names(query, topk=5):
    """법제처 OPEN API로 관련 법령명을 조회. 어떤 경우에도 리스트 반환(예외 없음)."""
    try:
        import httpx

        url = "https://www.law.go.kr/DRF/lawSearch.do"
        params = {"OC": LAW_OC, "target": "law", "query": str(query), "type": "JSON"}
        r = httpx.get(url, params=params, timeout=6.0)
        if r.status_code != 200:
            return []
        data = r.json()
        names = []

        def walk(obj):
            try:
                if isinstance(obj, dict):
                    nm = obj.get("법령명한글") or obj.get("법령명")
                    if nm:
                        names.append(str(nm).strip())
                    for v in obj.values():
                        walk(v)
                elif isinstance(obj, list):
                    for v in obj:
                        walk(v)
            except Exception:
                return

        walk(data)
        seen, uniq = set(), []
        for n in names:
            if n and n not in seen:
                seen.add(n)
                uniq.append(n)
        return uniq[:topk]
    except Exception:
        return []


