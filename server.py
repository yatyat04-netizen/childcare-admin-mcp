def _fetch_law_article(query):
    """법제처 lawService로 영유아보육법 조문 본문을 best-effort 조회. 연결 안 되면 None(예외 없음)."""
    try:
        import re as _re
        import httpx
        q = str(query)
        if "영유아보육" not in q and "보육법" not in q:
            return None
        m = _re.search(r"제?\s*(\d+)\s*조", q)
        if not m:
            return None
        jo = "%06d" % (int(m.group(1)) * 100)   # 제n조 → n00 (6자리)
        url = "https://www.law.go.kr/DRF/lawService.do"
        params = {"OC": LAW_OC, "target": "law", "type": "JSON",
                  "LM": "영유아보육법", "JO": jo}
        r = httpx.get(url, params=params, timeout=4.0)
        if r.status_code != 200:
            return None
        txt = []

        def walk(o):
            try:
                if isinstance(o, dict):
                    c = o.get("조문내용") or o.get("항내용")
                    if c:
                        txt.append(str(c))
                    for v in o.values():
                        walk(v)
                elif isinstance(o, list):
                    for v in o:
                        walk(v)
            except Exception:
                return

        walk(r.json())
        joined = "\n".join(t.strip() for t in txt if t and t.strip())
        return joined[:1500] if joined else None
    except Exception:
        return None


